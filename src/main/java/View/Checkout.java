    public void isi_cb_alamat() {
        DefaultComboBoxModel<String> model = new DefaultComboBoxModel<>();

        checkoutModel.listAlamat.clear();
        checkout_db.select_alamat(checkoutModel);
        model.addElement("Select Alamat");
        for (String alamat : checkoutModel.listAlamat) {
            model.addElement(alamat);
        }
        cb_alamat.setModel(model);
    }

    private void jLabel8MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jLabel8MouseClicked
        String alamat = JOptionPane.showInputDialog(this, "Masukan Alamat");

        if (alamat == null || alamat.trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Anda tidak memasukan alamat baru");
        } else {
            checkoutModel.setAlamat(alamat);
            jLabel8.setText(alamat);
            checkout_db.insert_alamat(checkoutModel);
            isi_cb_alamat();
            cb_alamat.setSelectedItem(alamat);
        }
    }//GEN-LAST:event_jLabel8MouseClicked

