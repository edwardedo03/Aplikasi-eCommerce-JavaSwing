    private void jPanelCustom1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jPanelCustom1MouseClicked
        ic.backgroundEntered(jPanelCustom1);
        if (cb_alamat.getSelectedItem().equals("Select Alamat") || metodePembayaran.getSelection() == null || cb_promo.getSelectedIndex() == 0 || cb_kurir.getSelectedIndex() == 0) {
            JOptionPane.showMessageDialog(this, "Pilih opsi checkout terlebih dahulu");
        } else {
            checkout_db.insertHistory(shoppingCartModel, checkoutModel);
            checkout_db.deleteCart_db();
            shoppingCartModel.listItem.clear();
            MainMenu.panelUtama.removeAll();
            MainMenu.panelUtama.add(shoppingCart);
            MainMenu.panelUtama.repaint();
            MainMenu.panelUtama.revalidate();
            shoppingCart.refreshShoppingCart();
        }
    }//GEN-LAST:event_jPanelCustom1MouseClicked


