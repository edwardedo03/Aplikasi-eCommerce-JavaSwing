    public void isiHistoryCart() {
        historyModel.listHistory.clear();
        db.selectListHistory(historyModel);
        String[][] dataTabel = new String [historyModel.listHistory.size()][4];
        
        int i = 0;
        
        for (HistoryModel history : historyModel.listHistory) {
            dataTabel[i][0] = "" + (i + 1);
            dataTabel[i][1] = String.valueOf(history.getTanggal());
            dataTabel[i][2] = df.format(history.getTotalHargaCart());
            dataTabel[i][3] = history.getAlamat();
            
            i++;
        }
        
        historyTable.setModel(new javax.swing.table.DefaultTableModel(
                dataTabel,
                new String[]{
                    "No.", "Tanggal", "Total Harga", "Alamat"
                }
        ) {
            boolean[] canEdit = new boolean[]{
                false, false, false, false
            };

            public boolean isCellEditable(int rowIndex, int columnIndex) {
                return canEdit[columnIndex];
            }
        });
    }

