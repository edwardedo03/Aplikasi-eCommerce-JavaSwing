    private void jPanelCustom1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jPanelCustom1MouseClicked
        int row = historyTable.getSelectedRow();
        
        if (row == -1) {
            JOptionPane.showMessageDialog(this, "Select item dalam history terlebih dahulu!", "SELECT HISTORY", JOptionPane.ERROR_MESSAGE);
        }
        
        HistoryModel history = historyModel.listHistory.get(row);
        Timestamp tanggal = history.getTanggal();
        String alamat = history.getAlamat();
        double totalHargaCart = history.getTotalHargaCart();

        
        HistoryDetail historyDetail = new HistoryDetail();
        historyDetail.isiHistoryDetail(historyModel, tanggal, alamat, totalHargaCart);
        
        MainMenu.panelUtama.removeAll();
        MainMenu.panelUtama.add(historyDetail);
        MainMenu.panelUtama.repaint();
        MainMenu.panelUtama.revalidate();
        
    }//GEN-LAST:event_jPanelCustom1MouseClicked

